/**
 * @file key.h
 * @brief 按键驱动头文件
 * 
 * 该文件定义了nRF52832芯片的GPIO按键接口相关声明和宏定义。
 * 使用P0端口的13-16引脚作为按键输入，每个按键独立控制一个LED灯。
 * 
 * 硬件连接：
 *   - 按键1(KEY1) 连接至 P0.13 引脚
 *   - 按键2(KEY2) 连接至 P0.14 引脚
 *   - 按键3(KEY3) 连接至 P0.15 引脚
 *   - 按键4(KEY4) 连接至 P0.16 引脚
 * 
 * 按键电路说明：
 *   - 按键采用低电平有效设计（按下为0，释放为1）
 *   - 每个按键均连接内部上拉电阻，无需外部上拉电阻
 *   - 当按键未按下时，GPIO读取高电平(1)
 *   - 当按键按下时，GPIO被拉低，读取低电平(0)
 * 
 * @note 本驱动使用软件消抖，通过延时函数消除机械按键的抖动干扰
 */

#include <stdint.h>

#ifndef _KEY_H
#define _KEY_H

/*==============================================*
 * 硬件引脚定义 - 使用P0端口（GPIO Port 0）    *
 *==============================================*/
/** @brief 按键1引脚 P0.13 */
#define KEY_1 13
/** @brief 按键2引脚 P0.14 */
#define KEY_2 14
/** @brief 按键3引脚 P0.15 */
#define KEY_3 15
/** @brief 按键4引脚 P0.16 */
#define KEY_4 16

/**
 * @brief 初始化所有按键
 * 
 * 配置P0.13~P0.16引脚为GPIO输入模式，并启用内部上拉电阻。
 * 调用此函数后，按键引脚将处于待检测状态。
 * 
 * @note 该函数应该在系统初始化阶段调用，通常在main函数开始时调用
 */
void KEY_Init(void);

/**
 * @brief 检测按键1是否按下（带消抖功能）
 * 
 * 检测P0.13引脚对应的按键1是否被按下。
 * 采用软件消抖机制：首先检测到低电平后延时一段时间，
 * 再次检测确认是否为真正的按键按下（非抖动）。
 * 消抖通过后会等待按键释放，确保每次按下只触发一次动作。
 * 
 * @return uint8_t 按键状态
 *         - 0: 按键已按下并释放（触发一次有效按键事件）
 *         - 1: 按键未按下或处于消抖等待中
 * 
 * @note 使用示例：
 *       if (KEY1_Down() == 0) {
 *           // 按键1被按下，执行对应操作
 *       }
 */
uint8_t KEY1_Down(void);

/**
 * @brief 检测按键2是否按下（带消抖功能）
 * 
 * 检测P0.14引脚对应的按键2是否被按下。
 * 功能与KEY1_Down()相同，适用于按键2。
 * 
 * @return uint8_t 按键状态
 *         - 0: 按键已按下并释放
 *         - 1: 按键未按下或处于消抖等待中
 * 
 * @see KEY1_Down()
 */
uint8_t KEY2_Down(void);

/**
 * @brief 检测按键3是否按下（带消抖功能）
 * 
 * 检测P0.15引脚对应的按键3是否被按下。
 * 功能与KEY1_Down()相同，适用于按键3。
 * 
 * @return uint8_t 按键状态
 *         - 0: 按键已按下并释放
 *         - 1: 按键未按下或处于消抖等待中
 * 
 * @see KEY1_Down()
 */
uint8_t KEY3_Down(void);

/**
 * @brief 检测按键4是否按下（带消抖功能）
 * 
 * 检测P0.16引脚对应的按键4是否被按下。
 * 功能与KEY1_Down()相同，适用于按键4。
 * 
 * @return uint8_t 按键状态
 *         - 0: 按键已按下并释放
 *         - 1: 按键未按下或处于消抖等待中
 * 
 * @see KEY1_Down()
 */
uint8_t KEY4_Down(void);

#endif /* __KEY_H */
